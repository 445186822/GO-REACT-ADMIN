import { writeFileSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';
import * as Icons from '@ant-design/icons';

const scriptDir = dirname(fileURLToPath(import.meta.url));
const frontendRoot = resolve(scriptDir, '..');
const outputPath = resolve(frontendRoot, 'src/features/menu/menuIcons.generated.tsx');

const iconNames = Object.keys(Icons)
  .filter((name) => name.endsWith('Outlined'))
  .sort((a, b) => a.localeCompare(b));

const importBlock = iconNames.map((name) => `  ${name},`).join('\n');
const rows = iconNames.map((name) => `  { name: '${name}', icon: <${name} /> },`).join('\n');

const source = `// This file is generated by scripts/generate-menu-icons.mjs. Do not edit manually.
import {
${importBlock}
} from '@ant-design/icons';
import type { ReactNode } from 'react';

export type GeneratedMenuIconDefinition = {
  name: string;
  icon: ReactNode;
};

export const generatedMenuIcons: GeneratedMenuIconDefinition[] = [
${rows}
];
`;

writeFileSync(outputPath, source, 'utf8');
console.log(`Generated ${iconNames.length} outlined menu icons at ${outputPath}`);
